    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        SimpleDateFormat sd=new SimpleDateFormat("dd-MM-yyyy");
        Date d=new Date();
        int h=0;
        try {
        DBConn db=new DBConn();
        db.pstmt=db.con.prepareStatement("select eid,datee from attendance where eid='"+jLabel71.getText()+"' and datee='"+sd.format(d)+"'");
        db.rst=db.pstmt.executeQuery();
        if(db.rst.next())
        {
            h=1;
        }
        } catch (Exception e) {
        e.printStackTrace();
        }
        if(h==1)
        {
        JOptionPane.showMessageDialog(this,"Attendance done already");    
        }
        else{
        try {
        DBConn db=new DBConn();
        db.pstmt=db.con.prepareStatement("insert into attendance values(?,?,?,?)");
        db.pstmt.setString(1,jLabel71.getText());
        db.pstmt.setString(2,jLabel72.getText());
        String a="";
        if(jCheckBox1.isSelected())
        {
            a="Full Time";
        }
        if(jCheckBox2.isSelected())
        {
            a="Half Time";
        }
        if(jCheckBox3.isSelected())
        {
            a="Over Time";
        }
        db.pstmt.setString(3,sd.format(d));
        db.pstmt.setString(4,a);
        int i=db.pstmt.executeUpdate();
        if(i>0)
        {
        JOptionPane.showMessageDialog(this,"Done..");
        jLabel71.setText("Employee Id");
        jLabel72.setText("Employee Name");
        jCheckBox1.setSelected(false);
        jCheckBox2.setSelected(false);
        jCheckBox3.setSelected(false);
        }
        } catch (Exception e) {
        e.printStackTrace();
        }}
    }//GEN-LAST:event_jButton5ActionPerformed
